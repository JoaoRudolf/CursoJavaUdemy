package streams.desafio;

public class Acao {
	
	final String nome;
	final int anoIpo;
	final boolean novoMercado;
	final int anosLucro;
	
	public Acao(String nome, int anoIpo, boolean novoMercado, int lucroDesde) {
		this.nome = nome;
		this.anoIpo = anoIpo;
		this.novoMercado = novoMercado;
		this.anosLucro = lucroDesde;
	}
	
	

}
