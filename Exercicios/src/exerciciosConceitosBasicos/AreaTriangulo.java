public class AreaTriangulo {

	public static void main(String[] args) {
		
		// A = (b * h) / 2
		
		double base = 12.0;
		double altura = 15.0;
		double multiplicacao = base * altura;
		
		double area = multiplicacao / 2;
		
		System.out.println("A área do triângulo é de " + area + " metros quadrados.");
	}
	
}
